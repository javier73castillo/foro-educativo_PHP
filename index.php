<div class="pri">
<h2> Este foro propone debatir el impacto de las tecnologías, los medios y dispositivos de comunicación e Internet, en la sociedad de hoy y en sus modos de relacionarse. <br><br> Las TIC como herramientas eficientes para el tratamiento de la información en la práctica docentes, el consumo y construcción del conocimiento, constituyen recursos estratégicos para la formación de formadores. <br><br> No sólo promueve el potencial de las tecnologías, como forma de apropiación y reflexión sobre los soportes y lenguajes utilizados, sino como un eje de la formación y de su práctica, que posibilite abordar el análisis y la reflexión.<br><br>	Esta plataforma contiene recursos para acompañar a los docentes de los distintos niveles educativos, a explorar propuestas e ideas que integran la educación digital. Hoy nos encontramos en el medio de un cambio de paradigma en la educación, dando cada vez más lugar a la tecnología de la información y comunicación. La propuesta de este espacio, tiende a brindar herramientas, materiales y propuestas, que permitan acompañar al docente en el camino de esta alfabetización, donde se descubren a menudo aplicaciones nuevas y metodologías novedosas que enriquecen las practicas docentes. <br><br>
</h2>
